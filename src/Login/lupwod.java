    private void btnverifikasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnverifikasiActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "SELECT * FROM akun where username = '"+txtusername.getText()+"' ";
            java.sql.Connection conn=(Connection)konek.configDBek();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery(sql);
            if (rs.next()){
                if(txtnik.getText().equals(rs.getString("nik")) && txtusername.getText().equals(rs.getString("username"))){
                    GlassPanePopup.showPopup(new luppas_true());
                    
                    txtnik.setEditable(false);
                    txtusername.setEditable(false);
                    txtpasswordbaru.setEditable(true);
                    txtpasswordbaru.requestFocus();
                    btnsimpan.setEnabled(true);
                    btnverifikasi.setEnabled(false);
                }
            }else{
                GlassPanePopup.showPopup(new luppas_false());
                txtusername.setText("");
                txtnik.setText("");
                txtnik.requestFocus();
            }
        } catch (Exception e) {
            System.out.println("gagal di konformasi");
        }
    }//GEN-LAST:event_btnverifikasiActionPerformed


